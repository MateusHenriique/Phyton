GRAU = float(input('digite um angulo: '))
import math
print('o seno do angulo {} é {:.2f}'.format(GRAU, math.sin(math.radians(GRAU))))
print('o cosseno do angulo {} é {:.2f}'.format(GRAU, math.cos(math.radians(GRAU))))
print('a tangente do angulo {} é {:.2f}'.format(GRAU, math.tan(math.radians(GRAU))))
      